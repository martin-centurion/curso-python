"""
Operadores de comparaciones
"""
# IGUAL "=="
# DISTINTO "!="
# OR "||"
# AND "&&"

a = 5
b = 6


# OPERADORES DE COMPARACIÓN
# Operador igual
print(a == b)
# Operador distinto
print(a != b)
# Operador menor, mayor o igual
print(a > b) # mayor
print(a >= b) # mayor e igual
print(a < b) # menor
print(a <= b) # menor e igual

#OPERADORES LÓGICOS
# OR "||" > or
# AND "&&" > and
# NOT > not

x = True
y = False

# OPERADOR AND / Y
print(x and y)
#OPERADOR OR / O
print(x or y)
# OPERADOR NEGACIÓN
print(not x)



