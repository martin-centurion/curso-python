#Conversiones de datos en Python, se puede cambiar un tipo de variable a otro.

numero_entero = "10"
print(numero_entero, " es del tipo", type(numero_entero))

#convertir un string a un entero
numero_entero = int("10")
print(numero_entero, " es del tipo", type(numero_entero))

#convertir un string a float

#numero_decimal = float("10.234343")
numero_decimal = float(5)
print(numero_decimal, " es del tipo", type(numero_decimal))

texto = str(123)
print(texto, " es del tipo", type(texto))